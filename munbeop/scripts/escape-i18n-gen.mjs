// Assemble per-locale translation dictionaries (.ts) for the
// escape-room seed from the canonical string list + the per-locale translations.
//
// Inputs (JSON, paths via argv):
//   argv[2] = escape-strings.json   -> { translatable: string[] , ... }
//   argv[3] = escape-translations.json -> { "<locale>": { "<index>": "<text>" } }
//   argv[4] = output dir (app/seed/escape-room/translations)
//
// Keys are taken from the CANONICAL list by index (never from agent output), so
// the lookup keys match the runtime es value byte-for-byte. Missing/empty entries
// are simply omitted -> they fall back to es at runtime.
import { readFileSync, writeFileSync, mkdirSync } from 'node:fs'
import { join } from 'node:path'

const [, , STRINGS, TRANSLATIONS, OUTDIR] = process.argv
if (!STRINGS || !TRANSLATIONS || !OUTDIR) {
  console.error('usage: node escape-i18n-gen.mjs <strings.json> <translations.json> <outdir>')
  process.exit(1)
}

const LOCALES = ['en', 'fr', 'pt-BR', 'th', 'id', 'vi', 'ja']
const IDENT = { en: 'en', fr: 'fr', 'pt-BR': 'ptBR', th: 'th', id: 'id', vi: 'vi', ja: 'ja' }

const canonical = JSON.parse(readFileSync(STRINGS, 'utf8')).translatable
const trAll = JSON.parse(readFileSync(TRANSLATIONS, 'utf8'))

mkdirSync(OUTDIR, { recursive: true })

const header = (locale) =>
  `// AUTO-GENERATED by scripts/escape-i18n-gen.mjs — do not edit by hand.\n` +
  `// Escape-room seed translations for "${locale}", keyed by the Spanish source\n` +
  `// string (the value passed to t()). Missing keys fall back to Spanish.\n\n`

const report = {}
for (const locale of LOCALES) {
  const map = trAll[locale] || {}
  const entries = []
  let missing = 0
  for (let i = 0; i < canonical.length; i++) {
    const v = map[String(i)]
    if (typeof v === 'string' && v.trim().length > 0) {
      entries.push([canonical[i], v])
    } else {
      missing++
    }
  }
  report[locale] = { translated: entries.length, missing }

  let body = header(locale)
  body += `export const ${IDENT[locale]}: Record<string, string> = {\n`
  for (const [k, v] of entries) {
    body += `  ${JSON.stringify(k)}: ${JSON.stringify(v)},\n`
  }
  body += `}\n`
  writeFileSync(join(OUTDIR, `${locale}.ts`), body, 'utf8')
}

// index.ts
let idx = `// AUTO-GENERATED by scripts/escape-i18n-gen.mjs — do not edit by hand.\n`
idx += `import type { LocaleCode } from '~/lib/domain'\n`
for (const locale of LOCALES) {
  idx += `import { ${IDENT[locale]} } from './${locale}'\n`
}
idx += `\n/**\n * Per-locale escape-room seed translations, keyed by Spanish source string.\n`
idx += ` * 'es' is the source-of-truth (empty map -> t() uses the literal es value).\n */\n`
idx += `export const TRANSLATIONS: Record<LocaleCode, Record<string, string>> = {\n`
idx += `  es: {},\n`
for (const locale of LOCALES) {
  idx += `  ${JSON.stringify(locale)}: ${IDENT[locale]},\n`
}
idx += `}\n`
writeFileSync(join(OUTDIR, 'index.ts'), idx, 'utf8')

console.log('canonical translatable:', canonical.length)
console.table(report)
